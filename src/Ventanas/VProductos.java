    private void btnGuardarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProductoActionPerformed
        // TODO add your handling code here:
        if (!"".equals(txtCodigoProducto.getText())&&!"".equals(txtDescripcionProducto.getText())&&!"".equals(txtCantidadProducto.getText())&&!"".equals(txtPrecioProducto.getText())&&!"".equals(txtGrupoProducto.getText())){
            pro.setCodigo(Integer.parseInt(txtCodigoProducto.getText()));
            pro.setNombre(txtDescripcionProducto.getText());
            pro.setGrupo(txtGrupoProducto.getText());
            pro.setStock(Integer.parseInt(txtCantidadProducto.getText()));
            pro.setPrecio(Double.parseDouble(txtPrecioProducto.getText()));
            proDAO.RegistrarProductos(pro);
            LimpiarTable();
            ListarProductos(); 
            LimpiarProductos();
            JOptionPane.showMessageDialog(null, "Producto registrado");
        }else{
            JOptionPane.showMessageDialog(null,"Los campos estan vacios");
        }
    }//GEN-LAST:event_btnGuardarProductoActionPerformed

