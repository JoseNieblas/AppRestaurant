    private void btnEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProductoActionPerformed
        // TODO add your handling code here:
        if("".equals(txtCodigoProducto.getText())){
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }else{
            if(!"".equals(txtCodigoProducto.getText())){
                int pregunta=JOptionPane.showConfirmDialog(null,"Esta seguro de eliminar? ");
                if(pregunta ==0){
                    int id= Integer.parseInt(txtCodigoProducto.getText());
                    proDAO.EliminarProductos(id);
                    LimpiarTable();
                    LimpiarProductos();
                    ListarProductos();
                }
            }
        }
    }//GEN-LAST:event_btnEliminarProductoActionPerformed

    private void btnActualizarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarProductoActionPerformed
        // TODO add your handling code here:
        if("".equals(txtCodigoProducto.getText())){
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }else{
            if(!"".equals(txtDescripcionProducto.getText())||!"".equals(txtCantidadProducto.getText())||!"".equals(txtPrecioProducto.getText())||!"".equals(txtGrupoProducto.getText())){
                pro.setNombre(txtDescripcionProducto.getText());
                pro.setGrupo(txtGrupoProducto.getText());
                pro.setStock(Integer.parseInt(txtCantidadProducto.getText()));
                pro.setPrecio(Double.parseDouble(txtPrecioProducto.getText()));
                pro.setCodigo(Integer.parseInt(txtCodigoProducto.getText()));
                proDAO.ActualizarProdutos(pro);
                LimpiarTable();
                LimpiarProductos();
                ListarProductos();
                JOptionPane.showMessageDialog(null,"Producto Modificado");
            }
        }
    }//GEN-LAST:event_btnActualizarProductoActionPerformed

