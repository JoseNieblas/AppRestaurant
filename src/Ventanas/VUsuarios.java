    private void TablaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaClienteMouseClicked
        int fila= TablaCliente.rowAtPoint(evt.getPoint());
        txtCodigoCliente.setText(TablaCliente.getValueAt(fila, 0).toString());
        txtNombreCliente.setText(TablaCliente.getValueAt(fila, 1).toString());
    }//GEN-LAST:event_TablaClienteMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if("".equals(txtCodigoCliente.getText())){
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }else{
            if(!"".equals(txtCodigoCliente.getText())){
                int pregunta=JOptionPane.showConfirmDialog(null,"Esta seguro de eliminar Mesero? ");
                if(pregunta ==0){
                    int codigo= Integer.parseInt(txtCodigoCliente.getText());
                    clDAO.EliminarCliente(codigo);
                    LimpiarTable();
                    ListarCliente();
                    LimpiarCliente();
                }
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarClienteActionPerformed
        if("".equals(txtCodigoCliente.getText())){
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }else{
            if(!"".equals(txtNombreCliente.getText())){
            cl.setNombre(txtNombreCliente.getText());
            cl.setCodigo(Integer.parseInt(txtCodigoCliente.getText()));
            clDAO.ActualizarCliente(cl);
            JOptionPane.showMessageDialog(null,"Mesero Modificado");
            LimpiarTable();
            LimpiarCliente();
            ListarCliente();
            }
        }
    }//GEN-LAST:event_btnActualizarClienteActionPerformed

