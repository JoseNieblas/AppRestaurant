    private void btnGuardarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarClientesActionPerformed
        if (!"".equals(txtCodigoCliente.getText()) && !"".equals(txtNombreCliente.getText())){
            cl.setCodigo(Integer.parseInt(txtCodigoCliente.getText()));
            cl.setNombre(txtNombreCliente.getText());
            clDAO.RegistrarCliente(cl);
            LimpiarTable();
            LimpiarCliente();
            ListarCliente();
            JOptionPane.showInternalMessageDialog(null, "Mesero Registrado");
        }else{
            JOptionPane.showMessageDialog(null, "Los campos estan vacios");
        }
    }//GEN-LAST:event_btnGuardarClientesActionPerformed

